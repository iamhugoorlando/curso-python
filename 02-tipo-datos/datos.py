# Aprenderemos sobre tipo de datos en Python

# Todos los valores de Python tienen un tipo de dato
# Los tipos de datos basicos son:
# Integer <int>
# Float <float>
# String <str>
# Boolean <boo>

# Con la funcion type() podremos saber que tipo de dato es nuestro valores

print(type('Hola Mundo'))
print(type(2))
print(type(2.9))
print(type(True))

print('Hola Mundo')
