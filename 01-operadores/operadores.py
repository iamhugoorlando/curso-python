# Aprenderemos sobre operadores matematicos en Python

# Nos permiten trabajar con valores y generar los mismos
# Nos permiten evaluar expresiones en el lenguaje
# Los operadores son:
# suma +, resta -, multi *, div /, modulo %
# potencia **, mayor >, menor <, igual ==

a = 2
b = 2
print(a+b) # sumamos a + b
print(a*b) # multiplicamos a * b
print(a/b) # dividimos a / b
print(a%b) # modulo de a entre b
print(a>b) # a mayor que b
print(a<b) # menor a menor que b
print(a==b) # a igual b
print(a**b) # a elevada a la potencia b
