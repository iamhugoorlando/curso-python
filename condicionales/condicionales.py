print(a == b)
print(a != b)
print(a < b)
print( a > b)
print( a <= b)
print( a >= b)

resul = a < 15 and b == 20
result = a == 15 or b > 20
resultado = a > 15 and b < 20
resultados = a == 'a' or b == 'b'

print(resul)
