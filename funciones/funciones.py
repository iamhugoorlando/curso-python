# Aprenderemos sobre funciones en Python

# una funcion es una secuencia de comandos que realizan una accion
# para definirla se utiliza el keyword "def"
# se le asigna un nombre y se define sus parametros
# las funciones son llamadas por su nombre y regresean un resultado


