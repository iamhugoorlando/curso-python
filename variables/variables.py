# Aprenderemos sobre variables y expresiones

# Las expresiones se componen de valores conectados por operadores
# Los valores pueden guardarse en variables
# Las variables se pueden reasignar

# Escribiremos las variables nombre, apellido, nick
nombre = 'Hugo'
apellido = 'Gonzalez'
nick = 'iamhugoorlando'
print(nombre)
print(apellido)
print(nick)

# Las variables deben tener un nombre significativo
# no puede referirse a una de las palabras reservadas de python

pi = 3.14159
radio = 3
area = pi * radio**2 # pi por radio al cuadrado
print(area)
